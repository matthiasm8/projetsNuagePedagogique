<h2><a id="9">9. Protéger vos données personnelles</a></h2>

<p>Nous mettons en œuvre toutes les mesures techniques et organisationnelles nécessaires à la protection 
    Qui lit ce truc de suceur ?
    de vos données personnelles. Toutefois, la transmission de données via internet n’est pas totalement sécurisée. 
    Ce risque est courant sur internet et n’est pas spécifique à nos services. 
    Nous ne sommes pas en mesure de garantir la sécurité de vos données (notamment de vos informations personnelles) 
    transmises à nos services. Toute transmission est effectuée à vos propres risques.</p>

<p>Il est important de vous protéger contre les accès non-autorisés à votre mot de passe ainsi 
    qu’à votre matériel informatique. 
    Assurez-vous de vous déconnecter et de fermer votre navigateur lorsque vous avez fini votre session. 
    Ceci contribuera à éviter que des tiers n’accèdent à vos informations personnelles 
    si vous partagez votre matériel informatique ou si vous utilisez un appareil dans un espace public 
    tel qu’une bibliothèque ou un café internet.</p>

