<h2><a id="1">1. La protection de vos données personnelles</a></h2>

<p>Dans le cadre de nos relations, nous collectons et traitons des données personnelles vous concernant. Ces données personnelles sont : toute information nous permettant de vous identifier en tant qu’individu : votre nom, votre prénom, votre email <br/>
<br/>
Nous nous engageons à protéger votre vie privée. Nous utilisons vos données personnelles conformément aux lois et règlements applicables relatifs à la protection et à la confidentialité des données, dont le Règlement Général européen sur la Protection des Données (RGPD).</p>

<p>Ce document précise :</p>

<ul>
 <li>Quel type de données personnelles nous collectons.</li>
 <li>Pourquoi nous les collectons.</li>
 <li>Comment nous les utilisons.</li>
 <li>Combien de temps nous les conservons.</li>
 <li>Qui est susceptible de les consulter.</li>
