<h2><a id="5">5. Combien de temps nous conservons vos données personnelles ?</a></h2>

<p>Nous sommes tenus par la loi de conserver vos données personnelles uniquement pendant la durée nécessaire au regard 
    de la finalité pour laquelle elles sont collectées. 
    La durée pendant laquelle vos données personnelles sont conservées sera déterminée par plusieurs critères, 
    notamment les raisons pour lesquelles nous utilisons les données, 
    la quantité et le caractère sensible ou non des informations, 
    le risque potentiel d’utilisation frauduleuse ou de divulgation des informations 
    ainsi que nos obligations légales et règlementaires.</p>

<h2><a id="6">6. Avec qui partageons-nous vos données personnelles ?</a></h2>

<p>Nous ne partageons pas vos données personnelles avec une autre organisation</p>

<h3>Où sont traitées vos données personnelles ?</h3>

<p>Vos données personnelles peuvent être transférées, conservées et traitées 
    dans un ou plusieurs pays en dehors de l’Espace Economique Européen, 
    notamment dans des pays qui n’offrent pas de protection équivalente des données personnelles. 
    Dans ces conditions, nous adoptons des mesures pour assurer une protection adéquate de vos données personnelles 
    conformément à la loi notamment grâce aux clauses contractuelles types de responsable du traitement à sous-traitant 
    émises par la Commission européenne et actuellement en vigueur.</p>

<h2><a id="7">7. Vos droits</a></h2>

<p>Vous disposez des droits suivants :</p>

<ul>
 <li>Accéder aux données personnelles que nous détenons.</li>
 <li>Corriger toute donnée personnelle erronée.</li>
 <li>Supprimer vos données personnelles en l’absence de motif légitime ou légal pour les conserver.</li>
 <li>Limiter le traitement de vos données.</li>
 <li>Vous opposer à l’utilisation de vos données personnelles, notamment concernant les traitements pour lesquels nous avons recueillis votre consentement, à des fins par exemple d’envoi de supports marketing tels que, nos catalogues, courriels, réductions ou bons d’achats.</li>
 <li>Retirer votre consentement à tout moment.</li>
 <li>Droit à la portabilité de vos données personnelles, communiquées de façon structurée et dans un format électronique 
     couramment utilisé. Ceci s’applique lorsque nous utilisons vos données personnelles 
     sur la base de votre consentement ou quand cela est nécessaire à l’exécution d’un contrat vous concernant 
     </li>
 <li>S’opposer à toute décision individuelle automatisée ou au profilage.</li>
 <li>Définir des directives relatives au traitement de vos données après votre décès.</li>
</ul>



